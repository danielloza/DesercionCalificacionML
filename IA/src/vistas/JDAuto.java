    private void btnCalcularAActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularAActionPerformed
        // TODO add your handling code here:
        NeuralNetwork neuralNetwork = NeuralNetwork.createFromFile("autoAutonomo_multilayer.nnet");

        // set network input
        int sensor = -1;
        int posicion = -1;

        if (cmbSensor.getSelectedItem() == "0.5") {
            sensor = 0;
        } else if (cmbSensor.getSelectedItem() == "1") {
            sensor = 1;
        } else {
            sensor = -1;
        }

        if (cmbPosicion.getSelectedItem() == "-1") {
            posicion = -1;
        } else if (cmbPosicion.getSelectedItem() == "0") {
            posicion = 0;
        } else {
            posicion = 1;
        }

        neuralNetwork.setInput(sensor, posicion);

        // calculate network 
        neuralNetwork.calculate();

        // get network output 
        double[] networkOutput = neuralNetwork.getOutput();
        int salida1 = (int) (networkOutput[0] + 0.5);
        int salida2 = (int) (networkOutput[1] + 0.5);

        if (salida1 == 1 && salida2 == 0) {
            txtResultadoA.setText("AVANZAR!!");
        } else if (salida1 == 1 && salida2 == 0) {
            txtResultadoA.setText("AVANZAR!!");
        } else if (salida1 == 1 && salida2 == 1) {
            txtResultadoA.setText("GIRAR A LA IZQUIERDA!!");
        } else if (salida1 == 0 && salida2 == 1) {
            txtResultadoA.setText("RETROCEDER!!");
        } else if (salida1 == 0 && salida2 == 0) {
            txtResultadoA.setText("RETROCEDER!!");
        }
    }//GEN-LAST:event_btnCalcularAActionPerformed

